    # Add new English keys to main dict (first occurrence wins)
    for en_verb, es_verb, fr_verb in EXTRA_TRANSLATIONS:
        if en_verb not in VERB_TRANSLATIONS:
            VERB_TRANSLATIONS[en_verb] = (es_verb, fr_verb)
